fn build_initial() -> InitialState {
	InitialState {
		player: INIT_PLAYER,
		human_list: INIT_HUMAN_LIST.to_vec(),
		zombie_list: INIT_ZOMBIE_LIST.to_vec(),
	}
}

fn build_actions() -> Vec<Vec<(i32, i32)>> {
	vec![SOLUTION.to_vec()]
}

#[test]
fn fast_kernel_score_matches_expected() {
	let initial = build_initial();
	let referee = Referee::new(&initial, SOLUTION.len()).expect("referee init");

	let score_list = referee.run(&build_actions()).expect("run");
	assert_eq!(score_list.len(), 1);
	let got_score = score_list[0];
	assert_eq!(
		got_score, FINAL_SCORE,
		"fast kernel score mismatch: got {got_score}, expected {FINAL_SCORE}"
	);
}

#[test]
fn debug_kernel_score_and_state_match_expected() {
	let initial = build_initial();
	let referee = Referee::new(&initial, SOLUTION.len()).expect("referee init");

	let (score_list, log) = referee
		.run_with_state(&build_actions())
		.expect("run_with_state");
	assert_eq!(score_list.len(), 1);
	let got_score = score_list[0];
	assert_eq!(
		got_score, FINAL_SCORE,
		"debug kernel score mismatch: got {got_score}, expected {FINAL_SCORE}"
	);

	let n_turns = SOLUTION.len();
	assert_eq!(EXPECTED_PLAYER.len(), n_turns);
	assert_eq!(EXPECTED_ALIVE_ZOMBIE_LIST.len(), n_turns);
	assert_eq!(EXPECTED_ZOMBIE_POSITIONS.len(), n_turns);
	assert_eq!(EXPECTED_ALIVE_HUMAN_LIST.len(), n_turns);

	for t in 0..n_turns {
		let (ax, ay) = log.player_pos(t, 0);
		let (ex, ey) = EXPECTED_PLAYER[t];
		assert_eq!(
			(ax as i32, ay as i32),
			(ex, ey),
			"player position mismatch at turn {t}: got ({ax}, {ay}), expected ({ex}, {ey})"
		);

		let alive_z = EXPECTED_ALIVE_ZOMBIE_LIST[t];
		let pos_z = EXPECTED_ZOMBIE_POSITIONS[t];
		assert_eq!(alive_z.len(), pos_z.len());

		for z in 0..INIT_ZOMBIE_LIST.len() {
			let should_be_alive = alive_z.contains(&z);
			let is_alive = log.is_zombie_alive(t, 0, z);
			assert_eq!(
				is_alive, should_be_alive,
				"zombie {z} alive flag mismatch at turn {t}: got {is_alive}, expected {should_be_alive}"
			);
		}
		for (idx, &zid) in alive_z.iter().enumerate() {
			let (zx, zy) = log.zombie_pos(t, 0, zid);
			let (ex, ey) = pos_z[idx];
			assert_eq!(
				(zx as i32, zy as i32),
				(ex, ey),
				"zombie {zid} position mismatch at turn {t}: got ({zx}, {zy}), expected ({ex}, {ey})"
			);
		}

		let alive_h = EXPECTED_ALIVE_HUMAN_LIST[t];
		for h in 0..INIT_HUMAN_LIST.len() {
			let should_be_alive = alive_h.contains(&h);
			let is_alive = log.is_human_alive(t, 0, h);
			assert_eq!(
				is_alive, should_be_alive,
				"human {h} alive flag mismatch at turn {t}: got {is_alive}, expected {should_be_alive}"
			);
		}
	}

	let final_score = log.turn_score(n_turns, 0);
	assert_eq!(
		final_score, FINAL_SCORE,
		"cumulative score after last turn mismatch: got {final_score}, expected {FINAL_SCORE}"
	);
}
